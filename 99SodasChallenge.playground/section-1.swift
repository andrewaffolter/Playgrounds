import UIKit


for(var numberOfSodas = 99; numberOfSodas >= 0; --numberOfSodas)
{
    if(numberOfSodas == 0)
    {
        println("No more bottles of soda on the wall")
    }
    else
    {
        println("\(numberOfSodas)" + " bottles of soda on the wall")
    }
    
}
